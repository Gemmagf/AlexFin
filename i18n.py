_fallback_extra = _EXTRA["it"]
for _lc in TRANSLATIONS:
    _src = _EXTRA.get(_lc, {})
    for _k, _v in _fallback_extra.items():
        TRANSLATIONS[_lc].setdefault(_k, _src.get(_k, _v))


def t(key: str, lang_code: str, **kwargs) -> str:
    """Get translated string. Falls back to Italian, then to the key itself."""
    lang_dict = TRANSLATIONS.get(lang_code, TRANSLATIONS["it"])
    val = lang_dict.get(key, TRANSLATIONS["it"].get(key, key))
    if kwargs:
        try:
            return val.format(**kwargs)
        except (KeyError, IndexError):
            return val
    return val


